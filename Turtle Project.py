# Let's draw some cool drawings with the package turtle
# Import turtle
import turtle
# Let's get a nice setup in turtle

turtle.bgcolor("black")  # Background colour
turtle.pensize(2)  # pen size
turtle.color("red")

# Draw a square
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.done()  # Allows turtle to stay on the screen

